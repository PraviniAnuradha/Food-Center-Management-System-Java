    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Brand name can't be empty", "Error", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet city_rs = MYSQL.search("SELECT * FROM `brand` WHERE `name`='" + name + "'");
                if (city_rs.next()) {
                    JOptionPane.showMessageDialog(this, "Brand  already added", "Error", JOptionPane.WARNING_MESSAGE);
                } else {

                    MYSQL.iud("INSERT INTO `brand`(`name`)VALUES('" + name + "')");
                    addproducts.loadbrand();
                    Home.notification = "New brand " + name + " added by admin " + Login.admin_username + " to the system ";
                    JOptionPane.showMessageDialog(this, "Successfully added", "Success", JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

