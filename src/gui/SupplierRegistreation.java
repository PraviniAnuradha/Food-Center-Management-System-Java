    public void loadsuppliers() {
        try {
            ResultSet rs = MYSQL.search("SELECT * FROM `supplier` INNER JOIN `company_branch`ON `supplier`.`company_branch_id`=`company_branch`.`id` INNER JOIN `company_branch_address`ON `company_branch`.`company_branch_address_id`=`company_branch_address`.`id` INNER JOIN `cities` ON `company_branch_address`.`cities_id`=`cities`.`id` ORDER BY `supplier`.`id` ASC");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0); //rows tika clear karana
            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("contact_number"));
                v.add(rs.getString("email"));
                v.add(rs.getString("company_branch.name"));
                v.add(rs.getString("company_branch.branch_contact_number"));
                String address = rs.getString("company_branch_address.line1") + "," + rs.getString("cities.name_en");
                v.add(address);
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

