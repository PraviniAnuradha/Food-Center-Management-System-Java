    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (jButton1.getText() == "Add") {
            //        user informations
            String product_name = jTextField1.getText();
            String product_code = jTextField2.getText();
            String category = jComboBox1.getSelectedItem().toString();
            String brand = jComboBox2.getSelectedItem().toString();

            if (product_name.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Product name can't be empty ", "Error", JOptionPane.WARNING_MESSAGE);
            } else if (product_code.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Product code can't be empty ", "Error", JOptionPane.WARNING_MESSAGE);
            } else if (category.equals("Select")) {
                JOptionPane.showMessageDialog(this, "Please select a category ", "Error", JOptionPane.WARNING_MESSAGE);
            } else if (brand.equals("Select")) {
                JOptionPane.showMessageDialog(this, "Please select a brand ", "Error", JOptionPane.WARNING_MESSAGE);
            } else {
                try {
                    ResultSet product_rs = MYSQL.search("SELECT * FROM `product` WHERE `name`='" + product_name + "' OR `code`='" + product_code + "'");
                    if (product_rs.next()) {
                        JOptionPane.showMessageDialog(this, "Product already exist", "Error", JOptionPane.WARNING_MESSAGE);
                    } else {
//                    category  id
                        ResultSet category_rs = MYSQL.search("SELECT * FROM `category` WHERE `name`='" + category + "'");
                        category_rs.next();
                        String category_id = category_rs.getString("id");

//                    brand  id
                        ResultSet brand_rs = MYSQL.search("SELECT * FROM `brand` WHERE `name`='" + brand + "'");
                        brand_rs.next();
                        String brand_id = brand_rs.getString("id");

                        MYSQL.iud("INSERT INTO `product`(`code`,`category_id`,`brand_id`,`name`)VALUES('" + product_code + "','" + category_id + "','" + brand_id + "','" + product_name + "')");
                        loadproducts();
                        resetfields();
                        Home.notification = "New product " + product_name + " added by admin " + Login.admin_username + " to the system ";
                        JOptionPane.showMessageDialog(this, "Product Registered", "Success", JOptionPane.INFORMATION_MESSAGE);

                    }

                } catch (Exception e) {
                    e.printStackTrace();
                }

            }
        } else if (jButton1.getText() == "Update") {
            //        user informations
            String product_name = jTextField1.getText();
            String product_code = jTextField2.getText();
            String category = jComboBox1.getSelectedItem().toString();
            String brand = jComboBox2.getSelectedItem().toString();

            try {
                //                    category  id
                ResultSet category_rs = MYSQL.search("SELECT * FROM `category` WHERE `name`='" + category + "'");
                category_rs.next();
                String category_id = category_rs.getString("id");

//                    brand  id
                ResultSet brand_rs = MYSQL.search("SELECT * FROM `brand` WHERE `name`='" + brand + "'");
                brand_rs.next();
                String brand_id = brand_rs.getString("id");

                MYSQL.iud("Update `product` SET `category_id`='" + category_id + "',`brand_id`='" + brand_id + "',`name`='" + product_name + "' WHERE `code`='" + product_code + "'");
                loadproducts();
                JOptionPane.showMessageDialog(this, "Product updated", "Success", JOptionPane.INFORMATION_MESSAGE);
                resetfields();
                jButton1.setText("Add");
                jTextField2.setEditable(true);

            } catch (Exception e) {
                e.printStackTrace();
            }

        }


    }//GEN-LAST:event_jButton1ActionPerformed

