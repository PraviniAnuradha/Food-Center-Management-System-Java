    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // TODO add your handling code here:
        String selected_value = (String) jComboBox1.getSelectedItem();

        if (selected_value.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select a Category", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet category_rs = MYSQL.search("SELECT * FROM `category` WHERE `name`='" + selected_value + "'");
                category_rs.next();
                int selected_id = category_rs.getInt("id");
//              get count
                ResultSet count_rs = MYSQL.search("SELECT COUNT(`category_id`) AS category_count FROM `product` WHERE `category_id`='" + selected_id + "'");
                count_rs.next();
                int category_count = count_rs.getInt("category_count");
//                generate code
                String firstLetter = selected_value.substring(0, 1);
                int result = 001 + category_count;
                String formattedResult = String.format("%03d", result);

                String generated_code = firstLetter + formattedResult;

                jTextField2.setText(generated_code);

            } catch (Exception ex) {
                Logger.getLogger(Addproducts.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_jButton10ActionPerformed

