    public void loadorders() {
        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            ResultSet rs = MYSQL.search("SELECT * FROM `invoice` INNER JOIN `customer` ON `invoice`.`customer_id`=`customer`.`id` INNER JOIN `address` ON `customer`.`id`=`address`.`customer_id` INNER JOIN `order_through` ON `invoice`.`order_through_id`=`order_through`.`id` WHERE `invoice`.`status`='Dilivered' ");
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("invoice.code"));
                v.add(rs.getString("invoice.date_time"));
                v.add(rs.getString("customer.name"));
                v.add(rs.getString("address.content"));
                v.add(rs.getString("order_through.name"));
                if (rs.getString("invoice.uniq_id").length() > 10) {
                    v.add("");
                } else {
                    v.add(rs.getString("invoice.uniq_id"));
                }
                dtm.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void chk_waybills() {

        try {
            ResultSet rs = MYSQL.search("SELECT * FROM `waybills_center`");
            rs.next();

            int last_number = rs.getInt("last_number");
            int end_number = rs.getInt("end_number");

            double percentageDiff = ((double) (end_number - last_number) / 500) * 100;

            System.out.println(percentageDiff);
            jProgressBar2.setValue((int) percentageDiff);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

