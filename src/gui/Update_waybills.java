    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            MYSQL.getConnection();
            ResultSet waybill_rs = MYSQL.search("SELECT * FROM `waybills_center`");

            if (waybill_rs.next()) {
                ResultSet rs = MYSQL.search("SELECT * FROM `invoice` WHERE `invoice`.`status`='Dilivered' ");
                int id = waybill_rs.getInt("last_number");
                boolean isfound = false;

                while (rs.next()) {

                    if (waybill_rs.getInt("end_number") < id) {
                        isfound = true;
                        break;
                    } else {
                        id = id + 1;
                        MYSQL.iud("UPDATE `invoice` SET `uniq_id`='" + id + "'  WHERE `id`='" + rs.getString("id") + "' ");
//                        update status
                        MYSQL.iud("UPDATE `invoice` SET `status`='Ready to print' WHERE `id`='" + rs.getString("id") + "'");
                    }

                }
                if (isfound) {
                    JOptionPane.showMessageDialog(this, "Waybill limit reach. Please update waybill center...", "Error", JOptionPane.WARNING_MESSAGE);

                } else {
                    // update waybill center
                    MYSQL.iud("UPDATE `waybills_center` SET `last_number`='" + id + "'");
                    Thread t = new Thread(() -> {
                        for (int i = 0; i <= 100; i++) {
                            jProgressBar1.setValue(i);
                            try {
                                Thread.sleep(5);
                            } catch (Exception e) {
                            }
                        }
                        //clear fileds
                        loadorders();
                        jProgressBar1.setValue(0);
                        JOptionPane.showMessageDialog(this, "Tracking number updated...", "Succcess", JOptionPane.INFORMATION_MESSAGE);
                        Print_invoice pri = new Print_invoice();
                        pri.setVisible(true);
                        this.dispose();
                    });
                    t.start();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Error Please update waybill center...", "Error", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        Home.notification = "Waybills are updated by admin " + Login.admin_username + " to the system ";

    }//GEN-LAST:event_jButton1ActionPerformed

